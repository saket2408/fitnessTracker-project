package com.example.demo.entity;

public class ErrorModel {

	String message;

	public ErrorModel() {
		super();
	}

	public ErrorModel(String message) {
		super();
		this.message = message;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}
	
}
